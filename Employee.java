public class Employee {
    private int empNum;
    private String empLastName;
    private String empFirstName;
    private double empSalary;

    public int getEmpNum() {
        return empNum;
    }

    public void setEmpNum(int emp) {
        empNum = emp;
    }

    public String getEmpLastName() {
        return empLastName;
    }

    public void setEmpLastName(String name) {
        empLastName = name;
    }
    public String getEmpFirstNameName() {
        return empFirstName;
    }

    public void setEmpFirstNameName(String name) {
        empFirstName = name;
    }

    public double setSalary(double sal) {
        return empSalary;
    }

    public void setEmpSalary(double sal) {
        empSalary = sal;
    }
}
